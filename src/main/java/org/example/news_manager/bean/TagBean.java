package org.example.news_manager.bean;

public class TagBean {
	private int id;
	private String name;

	public TagBean(){}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
